public class School {
	private int schoolId;
	private String schoolName;
	private int schoolProId;
	private int schoolSchoolTypeId;
	/**
	 * @return the schoolId
	 */
	public int getSchoolId() {
		return schoolId;
	}
	/**
	 * @param schoolId the schoolId to set
	 */
	public void setSchoolId(int schoolId) {
		this.schoolId = schoolId;
	}
	/**
	 * @return the schoolName
	 */
	public String getSchoolName() {
		return schoolName;
	}
	/**
	 * @param schoolName the schoolName to set
	 */
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	/**
	 * @return the schoolProId
	 */
	public int getSchoolProId() {
		return schoolProId;
	}
	/**
	 * @param schoolProId the schoolProId to set
	 */
	public void setSchoolProId(int schoolProId) {
		this.schoolProId = schoolProId;
	}
	/**
	 * @return the schoolSchoolTypeId
	 */
	public int getSchoolSchoolTypeId() {
		return schoolSchoolTypeId;
	}
	/**
	 * @param schoolSchoolTypeId the schoolSchoolTypeId to set
	 */
	public void setSchoolSchoolTypeId(int schoolSchoolTypeId) {
		this.schoolSchoolTypeId = schoolSchoolTypeId;
	}
	
}
